def get_figlet_logo(text='asleep'):
    figlet_fonts = ['slant', 'banner3', 'doom', 'big', 'standard', 'isometric1', 'larry3d']
    font = random.choice(figlet_fonts)
    try:
        return Figlet(font=font).renderText(text)
    except Exception:
        return Figlet(font='slant').renderText(text)
